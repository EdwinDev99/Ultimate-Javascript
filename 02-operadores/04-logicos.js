// console.log(true && true);
// console.log(false && true);
console.log("operador and", mayor && sucrito); //solo basta tener uno false para que sea false

// OR ||

console.log("operador or ", mayor || sucrito); //solo basta tener uno treu para que devuelva true

//  NOT !

console.log("operador NOT ", !mayor); //hace lo contrario si mayor es false devuelve true
