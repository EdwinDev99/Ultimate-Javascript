let a = 2;
let b = 4;

console.log(a + b); //suma
console.log(b - b); //resta
console.log(b * b); //multiplicacion
console.log(a / b); //divicion
console.log(a % b); //modulo (el restante)
console.log(a ** b); //potencia (elevado)

//incrementar
console.log(++a);
console.log(a++);
//decrementar
console.log(--a);
console.log(a--);
