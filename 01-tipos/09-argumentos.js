function suma(a, b) {
  // en estos parentecis redondos podemos pasarle parametros para que sea utilizados despues en otra function
  return a + b;
}

console.log(suma(2, 2)); // los valores que nostros le pasamos a las funciones cuando las llamamos se llaman argumentos las funciones puede recibir multiples argumentos y parametros

